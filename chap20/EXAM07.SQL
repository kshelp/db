SET SERVEROUTPUT ON
DECLARE
	VEMP EMP%ROWTYPE;
	VDNAME VARCHAR2(14);
BEGIN
	DBMS_OUTPUT.PUT_LINE('사번 / 이름 / 부서명');
	DBMS_OUTPUT.PUT_LINE('-------------------------------------');
	SELECT * INTO VEMP
		FROM EMP
		WHERE ENAME='SCOTT';
	IF (VEMP.DEPTNO = 10) THEN
		VDNAME := 'ACCOUNTING';
	ELSIF (VEMP.DEPTNO = 20) THEN
		VDNAME := 'RESEARCH';
	ELSIF (VEMP.DEPTNO = 30) THEN
		VDNAME := 'SALES';
	ELSIF (VEMP.DEPTNO = 40) THEN
		VDNAME := 'OPERATIONS';
	END IF;
	DBMS_OUTPUT.PUT_LINE(VEMP.EMPNO||'/'||VEMP.ENAME||'/'||VDNAME);
END;
/